function setPage(i){
    // render HTML
    var Page = `
        <li>
            <a  class="news-page-number" id ="page-number-${i}" onclick="clickPageNumbers()">${i}</a>
        </li>
    `
    $('.news-page-list-number').append(Page);
}
var listPages = [];
function getPageNumbers(){
    var cal = lengthListNews / pageSize ;
    if(lengthListNews % pageSize ==0){
        for(var i=1 ; i<=cal;i++){
            listPages.push(i);
        }
    }else{
        for(var i=1 ; i<=Math.floor(cal +1);i++){
            listPages.push(i);
        }
    }
    return listPages;
}

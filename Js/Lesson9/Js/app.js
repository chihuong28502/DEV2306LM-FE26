function setView(obj){
    var view = `
    <div class="news-item ">
            <div class="news-img">
                <img class="w-100" src="${obj.image}" alt="img-item">
            </div>
            <div class="news-content">
                <h3 class="news-text-title">${obj.title}</h3>
                <p class="news-text-content">${obj.content}</p>
            </div>
        </div>
    `
    $('.list-news').append(view);
}
function setListNew(arr,search){
    $('.list-news').html('')
    
    for(let i= 0 ; i<arr.length ; i++){
        if(arr[i].title == search || search == ""){ 
            setView(arr[i]);
        }
    }
}
function searchItem(){
    var title = $('#search').val();
    setListNew(listNews ,title)
}
